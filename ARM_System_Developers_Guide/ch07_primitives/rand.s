;// Section 7.8: Random number generators

        AREA    ch07_8, CODE, READWRITE

        EXPORT  rand_32

;=================================================================////
;// return a random number between 0 and n

n       RN 0
r       RN 1
a       RN 2
t       RN 3
        
        EXPORT  rand_32
rand_32
        ; load state
        LDR     a, = 0x91E6D6A5
        LDR     r, seed
        ; generate next random number
        MLA     r, a, r, a      ; r = r*a+a      
        UMULL   t, n, r, n      ; n = (r*n)>>32
        ; save state and return
        STR     r, seed
        MOV     pc, lr
        
seed    DCD 0x12345678  ; initial random seed

        END
